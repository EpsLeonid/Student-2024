function main_project()
    % Задаем параметры для исходной функции
    A = 1; 
    tau1 = 16;
    tau2 = 5;
    t = -10:1:100;  % шаг по t = 1 от -10 до 100

    % Генерация исходной функции
    v = A .* exp(-t / tau1) .* sin(t / tau2);

    % Построение графика исходной функции
    figure;
    plot(t, v);
    title('График функции f(t) = A * exp(-t/\tau1) * sin(t/\tau2)');
    xlabel('Время t');
    ylabel('Амплитуда f(t)');
    grid on;

    % Параметры трапецеидального фильтра
    k = 8;
    l = 5;
    M = 16;

    % Инициализация переменных для фильтрации
    p = zeros(size(v));
    s = zeros(size(v));
    dk_l = zeros(size(v));
    
    % Применение трапецеидального фильтра по формуле
    for n = max(k+l, 2):length(v)
        dk_l(n) = v(n) - v(n-k) - v(n-l) + v(n-k-l);
        p(n) = p(n-1) + dk_l(n);
        r = p(n) + M * dk_l(n);
        s(n) = s(n-1) + r;
    end

    % Построение графика после применения трапецеидального фильтра
    figure;
    plot(t, s);
    title('График после применения трапецеидального фильтра');
    xlabel('Время t');
    ylabel('Амплитуда после фильтрации');
    grid on;
end
